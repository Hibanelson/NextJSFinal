export function getMoviesByCategory(categorySlug: string): Movie[] {
  return movies.filter((m) => m.categories.includes(categorySlug));
}

export function getFeaturedMovies(): Movie[] {
  return movies.filter((m) => m.featured);
}

export function getMovieById(id: number): Movie | undefined {
  return movies.find((m) => m.id === id);
}

export function searchMovies(query: string): Movie[] {
  const q = query.toLowerCase();
  return movies.filter(
    (m) =>
      m.title.toLowerCase().includes(q) ||
      m.genres.some((g) => g.toLowerCase().includes(q)) ||
      m.overview.toLowerCase().includes(q)
  );
}

export function getSimilarMovies(movie: Movie): Movie[] {
  return movies
    .filter(
      (m) =>
        m.id !== movie.id &&
        m.genres.some((g) => movie.genres.includes(g))
    )
    .slice(0, 8);
}
